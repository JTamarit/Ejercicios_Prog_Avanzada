    def _get_data_time(self,data,format):
        if format == "full_data":
            timestamp = datetime.datetime.fromtimestamp(data)
            _data_time= str(timestamp.strftime('%d-%m-%Y %H:%M'))
            return _data_time
        elif format == "day":
            timestamp = datetime.datetime.fromtimestamp(data)
            _data_time= str(timestamp.strftime('%d-%m'))
            return _data_time
        elif format == "hour":
            timestamp = datetime.datetime.fromtimestamp(data)
            _data_time= str(timestamp.strftime('%H:%M'))
            return _data_time

